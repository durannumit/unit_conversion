import 'dart:math' as math;

class Units {

  // final unit values
  static double twoPi = 2.0 * math.pi;
  static double totalDegrees = 360.0;
  static double atmospherePascals = 101325.0;
  static double degreesToRadians = math.pi / 180.0;
  static double milesToKilometers = 1.609344;
  static double milesToMeters = 1609.344;
  static double kilometersToMiles = 1.0 / milesToKilometers;
  static double celsiusToKelvin = 273.15;
  static double meanEarthRadiusInKilometers = 6371.0;
}
