import 'dart:math' as math;

class Units {

  /// final unit values
  static final double twoPi = 2.0 * math.pi;
  static final double totalDegrees = 360.0;
  static final double atmospherePascals = 101325.0;
  static final double degreesToRadians = math.pi / 180.0;
  static final double milesToKilometers = 1.609344;
  static final double milesToMeters = 1609.344;
  static final double kilometersToMiles = 1.0 / milesToKilometers;
  static final double celsiusToKelvin = 273.15;
  static final double meanEarthRadiusInKilometers = 6371.0;
}
